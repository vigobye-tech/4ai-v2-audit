# Simple PowerShell script to create a basic ICO file
$iconData = @(
    0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x20, 0x20, 0x00, 0x00, 0x01, 0x00, 0x20, 0x00, 0x30, 0x10,
    0x00, 0x00, 0x16, 0x00, 0x00, 0x00, 0x28, 0x00, 0x00, 0x00, 0x20, 0x00, 0x00, 0x00, 0x40, 0x00,
    0x00, 0x00, 0x01, 0x00, 0x20, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x10, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00
)

# Add more icon data - this is a minimal 32x32 icon header
for ($i = 0; $i -lt (32*32*4); $i++) {
    if ($i % 4 -eq 3) {
        $iconData += 0xFF  # Alpha channel - opaque
    } elseif ($i % 16 -lt 8) {
        $iconData += 0x3F  # Blue-ish color
    } else {
        $iconData += 0x7F  # Different shade
    }
}

# Write to ICO file
[System.IO.File]::WriteAllBytes("c:\4AI\2.0\src-tauri\icons\icon.ico", $iconData)
Write-Host "Basic ICO file created"
